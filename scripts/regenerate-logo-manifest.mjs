// Scans public/logos/ and (over)writes src/services/logoManifest.ts.
// Run after any change to public/logos/ — the file is committed to git so the
// frontend can resolve ticker → logo URL at build time without round-tripping
// through Firestore.
//
// Called automatically at the end of bulk-fetch-spine-logos.mjs,
// bulk-fetch-cashflow-logos.mjs, and update-logo.mjs.
//
// Manual usage:
//   node scripts/regenerate-logo-manifest.mjs

import { readdirSync, writeFileSync } from 'node:fs'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const projectRoot = resolve(dirname(fileURLToPath(import.meta.url)), '..')
const logosDir = resolve(projectRoot, 'public', 'logos')
const outFile = resolve(projectRoot, 'src', 'services', 'logoManifest.ts')

const entries = readdirSync(logosDir)
  .filter((f) => !f.startsWith('.') && f !== 'manifest.json')
  .map((f) => {
    const dot = f.lastIndexOf('.')
    if (dot <= 0) return null
    const safe = f.slice(0, dot).toUpperCase()
    return [safe, `/logos/${f}`]
  })
  .filter(Boolean)
  .sort(([a], [b]) => a.localeCompare(b))

const body = entries.map(([t, url]) => `  '${t}': '${url}',`).join('\n')

const ts = `// AUTO-GENERATED by scripts/regenerate-logo-manifest.mjs — do not edit by hand.
// Source of truth: public/logos/. Re-run the generator after any change.

export const LOGO_MANIFEST: Record<string, string> = {
${body}
}

export function getLogoUrl(ticker: string | null | undefined): string | null {
  if (!ticker) return null
  const safe = String(ticker).replace(/[^A-Za-z0-9._-]/g, '_').toUpperCase()
  return LOGO_MANIFEST[safe] ?? null
}
`

writeFileSync(outFile, ts)
console.log(`[logo-manifest] wrote ${entries.length} entries to ${outFile.replace(projectRoot + '/', '')}`)
